function copyTextDiv(elementid,copy_tooltip) {
  var copyText = document.getElementById(elementid);
  navigator.clipboard.writeText(copyText.innerHTML);
  var tooltip = document.getElementById(copy_tooltip);
  // tooltip.innerHTML = "Copied: " + copyText.innerHTML;
    tooltip.innerHTML = "Copied ";
}

function outText(tooltipID) {
  var tooltip = document.getElementById(tooltipID);
  tooltip.innerHTML = "Copy Address";
}

function startTimer0(){
  // Set the date we're counting down to
var countDownDate = new Date("Oct 21, 2022 15:00:00").getTime();

// Update the count down every 1 second
var x = setInterval(function() {

  // Get today's date and time
  var now = new Date().getTime();
    
  // Find the distance between now and the count down date
  var distance = countDownDate - now;
    
  // Time calculations for days, hours, minutes and seconds
  var days = Math.floor(distance / (1000 * 60 * 60 * 24));
  var hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
  var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
  var seconds = Math.floor((distance % (1000 * 60)) / 1000);
    
  // Output the result in an element with id="demo"
  document.getElementById("timer0").innerHTML = days + "d " + hours + "h "
  + minutes + "m " + seconds + "s ";
    
  // If the count down is over, write some text 
  if (distance < 0) {
    clearInterval(x);
    document.getElementById("timer0").innerHTML = "EXPIRED";
  }
}, 1000);
}

